const { ApplicationCommandOptionType, EmbedBuilder } = require('discord.js');
const { QueryType, useMainPlayer, useQueue   } = require('discord-player');

module.exports = {
    name: 'playnext',
    description: "m√∫sica que voc√™ deseja tocar a seguir",
    voiceChannel: true,
    options: [
        {
            name: 'song',
            description: 'a m√∫sica que voc√™ deseja tocar a seguir',
            type: ApplicationCommandOptionType.String,
            required: true,
        }
    ],

    async execute({ inter }) {
        const player = useMainPlayer()

const queue = useQueue(inter.guild);

        if (!queue || !queue.isPlaying()) return inter.editReply({ content: `N√£o h√° m√∫sica tocando no momento ${inter.member}... tentar novamente ? ‚ùå`, ephemeral: true });

        const song = inter.options.getString('song');

        const res = await player.search(song, {
            requestedBy: inter.member,
            searchEngine: QueryType.AUTO
        });

        if (!res || !res.tracks.length) return inter.editReply({ content: `Nenhum resultado encontrado ${inter.member}... tentar novamente ? ‚ùå`, ephemeral: true });

       if (res.playlist) return inter.editReply({ content: `Este comando n√£o suporta playlists ${inter.member}... tentar novamente? ‚ùå`, ephemeral: true });

        queue.insertTrack(res.tracks[0], 0)

        const PlayNextEmbed = new EmbedBuilder()
        .setAuthor({name: `A m√∫sica foi inserida na fila... ela tocar√° a seguir üéß` })
        .setColor('#2f3136')
        
        await inter.editReply({ embeds: [PlayNextEmbed] });


    }
}
